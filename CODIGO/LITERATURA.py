from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def literatura():
    INICIO(MATERIA="LITERATURA")

    QUESTAO('''01) Quem é o autor da obra "Dom Casmurro", considerada um dos principais romances da literatura brasileira?
        A) Machado de Assis.
        B) José de Alencar.
        C) Lima Barreto.
        D) Euclides da Cunha.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=1)

    QUESTAO('''02) Qual é a protagonista do romance "Romeu e Julieta", de William Shakespeare?
        A) Julieta.
        B) Ofélia.
        C) Desdêmona.
        D) Cordélia.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=2)

    QUESTAO('''03) Quem é o autor da obra "Grande Sertão: Veredas", que retrata o sertão brasileiro e é considerada uma das mais importantes da literatura nacional?
        A) Guimarães Rosa.
        B) Euclides da Cunha.
        C) João Cabral de Melo Neto.
        D) José de Alencar.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=3)

    QUESTAO('''04) Qual é a protagonista do livro "Orgulho e Preconceito", de Jane Austen?
        A) Emma Woodhouse.
        B) Anne Elliot.
        C) Elizabeth Bennet.
        D) Elinor Dashwood.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) Quem é o autor da peça teatral "Othello", também conhecida como "Otelo"?
        A) William Shakespeare.
        B) Anton Tchekhov.
        C) Molière.
        D) Henrik Ibsen.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) Qual é a obra do escritor Franz Kafka que conta a história de um homem que acorda transformado em um inseto?
        A) "O Processo".
        B) "A Metamorfose".
        C) "O Castelo".
        D) "O Desaparecido".
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) Quem é a autora da obra "Harry Potter", uma série de sete livros que se tornou um fenômeno mundial?
        A) J.K. Rowling.
        B) George R.R. Martin.
        C) J.R.R. Tolkien.
        D) Suzanne Collins.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=7)

    QUESTAO('''08) Qual é o autor da peça teatral "Esperando Godot", que é considerada um marco do teatro do absurdo?
        A) Samuel Beckett.
        B) Harold Pinter.
        C) Eugene Ionesco.
        D) Jean-Paul Sartre.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=8)

    QUESTAO('''09) Quem é o autor da obra "Crime e Castigo", um dos grandes romances da literatura russa?
        A) Fiódor Dostoiévski.
        B) Lev Tolstói.
        C) Anton Tchekhov.
        D) Ivan Turguêniev.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=9)

    QUESTAO('''10) Qual é a protagonista da obra "A Moreninha", de Joaquim Manuel de Macedo?
        A) Carolina.
        B) Julieta.
        C) Maria.
        D) Carolina.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=10)

    FIM()
