from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def direitos():
    INICIO(MATERIA="DIREITOS")

    QUESTAO('''01) O que são direitos fundamentais, conforme a Constituição Brasileira?
        A) Privilégios concedidos apenas a determinados grupos.
        B) Direitos concedidos apenas aos cidadãos nascidos no país.
        C) Direitos inerentes à dignidade da pessoa humana e essenciais para sua plena realização.
        D) Normas aplicáveis apenas em situações excepcionais.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=1)

    QUESTAO('''02) Qual é a função do habeas corpus?
        A) Garantir o direito à propriedade.
        B) Proteger contra prisões ou detenções ilegais.
        C) Assegurar o direito ao trabalho.
        D) Estabelecer o direito à educação.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) O que é o princípio da presunção de inocência?
        A) Suposição de culpa até prova em contrário.
        B) Suposição de inocência até prova em contrário.
        C) Presunção de culpa independente de provas.
        D) Presunção de inocência apenas para casos graves.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Qual é o direito previsto no inciso VIII do artigo 5º da Constituição Federal, que assegura o direito de resposta, proporcional ao agravo, além da indenização por dano material, moral ou à imagem?
        A) Direito à vida.
        B) Direito à liberdade.
        C) Direito à honra.
        D) Direito à propriedade.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) O que é a cláusula pétrea na Constituição Federal?
        A) Norma que pode ser alterada por emendas constitucionais.
        B) Norma que não pode ser alterada por emendas constitucionais.
        C) Norma que trata de assuntos secundários.
        D) Norma exclusiva para casos de emergência.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) O que é o habeas data?
        A) Direito de resposta a informações falsas.
        B) Direito de acesso a informações pessoais constantes em registros públicos.
        C) Direito de ir e vir.
        D) Direito à privacidade na internet.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que é o princípio da irretroatividade da lei penal?
        A) Aplicação da lei apenas em casos anteriores à sua criação.
        B) Aplicação da lei apenas em casos futuros à sua criação.
        C) Retroatividade da lei penal apenas para crimes graves.
        D) Retroatividade da lei penal apenas para beneficiar o réu.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) O que é o habeas corpus preventivo?
        A) Garantia contra prisões ilegais que já ocorreram.
        B) Garantia contra prisões ilegais que ainda podem ocorrer.
        C) Garantia apenas para crimes de menor gravidade.
        D) Garantia apenas para crimes violentos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) O que são direitos sociais na Constituição Brasileira?
        A) Direitos concedidos apenas a determinadas classes sociais.
        B) Direitos relacionados apenas à liberdade de expressão.
        C) Direitos fundamentais relacionados ao bem-estar e à igualdade social.
        D) Direitos exclusivos para casos de emergência.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) Qual é o princípio jurídico que assegura a todos o direito ao meio ambiente ecologicamente equilibrado, impondo ao Poder Público e à coletividade o dever de defendê-lo e preservá-lo para as presentes e futuras gerações?
        A) Princípio da legalidade.
        B) Princípio da sustentabilidade.
        C) Princípio da dignidade da pessoa humana.
        D) Princípio do desenvolvimento sustentável.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=10)

    FIM()
