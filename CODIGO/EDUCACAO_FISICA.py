from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def educacao_fisica():
    INICIO(MATERIA="EDUCAÇÃO FÍSICA")

    QUESTAO('''01) Explique o conceito de periodização do treinamento esportivo e como ela pode ser aplicada para otimizar o desempenho atlético.
        A) Método que visa apenas a diversificação das atividades físicas.
        B) Estratégia de planejamento que organiza o treinamento em diferentes fases ao longo do tempo, visando melhorar o condicionamento e prevenir o overtraining.
        C) Abordagem que ignora a individualidade biológica dos atletas.
        D) Técnica exclusiva para atletas profissionais.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) O que é a aptidão física e quais são os componentes principais que a compõem?
        A) Aptidão física refere-se apenas à resistência cardiovascular.
        B) Capacidade de executar tarefas do dia a dia sem fadiga. Componentes: força, resistência cardiovascular, flexibilidade e composição corporal.
        C) Exclusivamente relacionada à força muscular.
        D) Aptidão física é sinônimo de habilidades esportivas.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) Explique o conceito de biomecânica aplicada à Educação Física e como ela pode ser utilizada para aprimorar o desempenho esportivo.
        A) Estudo exclusivo de lesões esportivas.
        B) Aplicação de princípios mecânicos ao movimento humano, visando otimizar a eficiência e prevenir lesões.
        C) Ciência que se concentra apenas na anatomia humana.
        D) Abordagem que ignora a relação entre a física e o movimento humano.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Qual é a importância da flexibilidade na prática de atividades físicas e como ela pode ser desenvolvida?
        A) Flexibilidade não é um componente relevante para o condicionamento físico.
        B) Contribui para a prevenção de lesões, melhora o desempenho atlético e pode ser desenvolvida por meio de exercícios de alongamento regular.
        C) Flexibilidade está relacionada apenas à performance em atividades como dança e ginástica.
        D) Não é possível desenvolver a flexibilidade por meio de treinamento.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=4)

    QUESTAO('''05) O que são os testes de avaliação física e qual é o seu papel na prescrição de exercícios?
        A) Testes de avaliação física são exclusivos para atletas profissionais.
        B) Ferramenta utilizada para medir a aptidão física, identificar áreas de melhoria e orientar o desenvolvimento de programas de treinamento personalizados.
        C) Testes não são relevantes para a prescrição de exercícios.
        D) Testes de avaliação física são aplicáveis apenas a indivíduos sedentários.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) Explique o conceito de treinamento funcional e como ele difere de abordagens tradicionais de musculação.
        A) Treinamento funcional é exclusivamente destinado a atletas de alto rendimento.
        B) Abordagem que se concentra em movimentos naturais do corpo, integrando força, equilíbrio, flexibilidade e coordenação. Difere de abordagens tradicionais por priorizar a funcionalidade nos gestos motores.
        C) Treinamento funcional não é aplicável a idosos.
        D) Método de treinamento que enfatiza apenas a hipertrofia muscular.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que são os métodos de treinamento de resistência e como eles podem ser aplicados para promover ganhos de força e hipertrofia muscular?
        A) Métodos de treinamento de resistência são irrelevantes para o desenvolvimento muscular.
        B) Estratégias como treino com pesos, circuitos, pirâmides e superséries. Cada método visa diferentes objetivos, como aumento de força, resistência ou hipertrofia.
        C) Treinamento de resistência é exclusivo para atletas de alto nível.
        D) Métodos de treinamento de resistência só são eficazes para a perda de peso.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) Explique o conceito de fadiga muscular e como ela pode impactar o desempenho durante o exercício físico.
        A) Fadiga muscular é exclusivamente psicológica e não afeta o desempenho físico.
        B) Fenômeno que resulta da acumulação de subprodutos metabólicos durante o exercício, levando à diminuição temporária da capacidade muscular. Pode afetar a força, resistência e coordenação.
        C) Fadiga muscular é irrelevante para atletas de resistência.
        D) Apenas iniciantes em exercícios físicos experimentam fadiga muscular.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) Qual é a importância da nutrição esportiva para o desempenho atlético e como os atletas podem otimizar sua alimentação para atender às demandas do treinamento?
        A) Nutrição esportiva é irrelevante para o desempenho atlético.
        B) Contribui para a recuperação, energia e melhoria do desempenho. Os atletas devem adequar a ingestão de macronutrientes e micronutrientes às necessidades do treinamento.
        C) Atletas não precisam se preocupar com a alimentação antes e após os treinos.
        D) Nutrição esportiva se concentra apenas na suplementação de vitaminas.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) Explique a importância do aquecimento e do alongamento na prevenção de lesões durante a prática de atividades físicas.
        A) Aquecimento e alongamento não têm impacto na prevenção de lesões.
        B) Aquecimento prepara o corpo para o exercício, aumentando a temperatura corporal e a circulação sanguínea. O alongamento melhora a flexibilidade e reduz o risco de lesões musculares.
        C) Aquecimento é dispensável para atividades de baixa intensidade. O alongamento não influencia na prevenção de lesões.
        D) Aquecimento é importante apenas em climas frios.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
