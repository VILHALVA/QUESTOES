from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def civismo():
    INICIO(MATERIA="CIVISMO")

    QUESTAO('''01) Qual é o nome técnico dado ao processo de emenda da Constituição Federal?
        A) Alteração Constitucional
        B) Reforma Constitucional
        C) Modificação Legislativa
        D) Atualização Normativa
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Quem tem o poder de propor uma emenda à Constituição Federal no Brasil?
        A) Presidente da República
        B) Senadores
        C) Deputados Federais
        D) Todos os cidadãos brasileiros
    ''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) O que é a cláusula pétrea na Constituição Federal?
        A) Normas que podem ser alteradas apenas por referendo popular.
        B) Disposições que tratam de assuntos considerados essenciais e imutáveis.
        C) Artigos que regulam exclusivamente questões econômicas.
        D) Resoluções do Poder Judiciário.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Qual é o papel do Tribunal de Contas da União (TCU) na fiscalização do governo?
        A) Julgar crimes comuns praticados por autoridades.
        B) Elaborar leis orçamentárias.
        C) Controlar a constitucionalidade das leis.
        D) Fiscalizar a aplicação dos recursos públicos.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=4)

    QUESTAO('''05) O que é o mandado de segurança, conforme previsto na Constituição Federal?
        A) Ação judicial para proteger direitos líquidos e certos.
        B) Medida provisória para garantir a segurança nacional.
        C) Ordem executiva do Presidente da República.
        D) Decisão do Supremo Tribunal Federal.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) O que é o habeas corpus e qual a sua finalidade?
        A) Medida de segurança para proteger autoridades públicas.
        B) Instrumento para garantir o direito à liberdade de locomoção.
        C) Pedido de impeachment do Presidente da República.
        D) Ação de censura a atos do Poder Executivo.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) Quais são as funções atribuídas ao Senado Federal no processo legislativo brasileiro?
        A) Propor e votar leis.
        B) Fiscalizar as contas do governo.
        C) Aprovar emendas constitucionais.
        D) Exercer a função de representação diplomática.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=7)

    QUESTAO('''08) O que é o voto de qualidade em decisões colegiadas?
        A) Voto que decide em caso de empate.
        B) Voto de destaque dado pelo presidente da câmara.
        C) Voto secreto dos parlamentares.
        D) Voto dado por cidadãos com mais de 60 anos.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=8)

    QUESTAO('''09) Qual é o papel das medidas provisórias no sistema legislativo brasileiro?
        A) Proporcionar maior agilidade nas decisões do Congresso Nacional.
        B) Substituir o papel do Senado Federal nas votações.
        C) Criar leis de caráter permanente.
        D) Regulamentar matérias orçamentárias.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=9)

    QUESTAO('''10) O que é o princípio da anualidade orçamentária na Constituição Federal?
        A) O orçamento deve ser aprovado anualmente pelo Congresso Nacional.
        B) As despesas públicas devem ser pagas anualmente.
        C) O Presidente da República deve prestar contas anualmente ao Tribunal de Contas da União.
        D) O orçamento é único e não sofre alterações anuais.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=10)

    FIM()
