from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def matematica():
    INICIO(MATERIA="MATEMÁTICA")

    QUESTAO('''01) Qual é a solução da equação \(3x - 2 = 7\)?
        A) \(x = 5\)
        B) \(x = 3\)
        C) \(x = 2\)
        D) \(x = 1\)
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Seja \(f(x) = 2x^2 + 3x - 5\). Qual é o valor de \(f(3)\)?
        A) 16
        B) 20
        C) 22
        D) 26
    ''')
    RESPOSTA(CERTA="A", QUESTAO=2)

    QUESTAO('''03) Qual é a expressão fatorada de \(x^2 - 4\)?
        A) \((x - 2)(x + 2)\)
        B) \((x - 1)(x + 1)\)
        C) \((x - 4)(x + 4)\)
        D) \((x - 3)(x + 3)\)
    ''')
    RESPOSTA(CERTA="A", QUESTAO=3)

    QUESTAO('''04) Se \(a = 4\) e \(b = -2\), qual é o valor de \(\frac{a}{b} - \frac{b}{a}\)?
        A) \(-3\)
        B) \(-2\)
        C) \(-1\)
        D) \(0\)
    ''')
    RESPOSTA(CERTA="A", QUESTAO=4)

    QUESTAO('''05) Se \(sen(x) = \frac{\sqrt{3}}{2}\) e \(x\) pertence ao segundo quadrante, qual é o valor de \(cos(x)\)?
        A) \(-\frac{1}{2}\)
        B) \(-\frac{\sqrt{3}}{2}\)
        C) \(\frac{1}{2}\)
        D) \(\frac{\sqrt{3}}{2}\)
    ''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) Uma loja vende um produto por R$ 1200,00 à vista ou em 3 parcelas iguais, sem juros, de R$ 450,00 cada. Qual é a taxa percentual de desconto oferecida para pagamento à vista?
        A) 5%
        B) 10%
        C) 15%
        D) 20%
    ''')
    RESPOSTA(CERTA="C", QUESTAO=6)

    QUESTAO('''07) Qual é a média aritmética simples dos números \(4, 8, 12, 16\) e \(20\)?
        A) \(12\)
        B) \(14\)
        C) \(16\)
        D) \(18\)
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) Se \(log_2 a = 3\), qual é o valor de \(a\)?
        A) \(2\)
        B) \(6\)
        C) \(8\)
        D) \(16\)
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) Uma piscina retangular tem as dimensões de \(4m \times 8m \times 2m\). Qual é o volume dessa piscina?
        A) \(16m^3\)
        B) \(32m^3\)
        C) \(48m^3\)
        D) \(64m^3\)
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) Se \(x^2 + y^2 = 25\), e \(x + y = 7\), qual é o valor de \(xy\)?
        A) \(-12\)
        B) \(-10\)
        C) \(10\)
        D) \(12\)
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
