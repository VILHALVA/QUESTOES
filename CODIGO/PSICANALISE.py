from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def psicanalise():
    INICIO(MATERIA="PSICANÁLISE")

    QUESTAO('''01) Qual é o conceito de inconsciente na teoria psicanalítica de Sigmund Freud?
        A) Área da mente que contém apenas pensamentos conscientes.
        B) Parte da mente que armazena memórias recentes.
        C) Aspecto da mente que contém desejos, memórias e pensamentos não acessíveis à consciência.
        D) Região da mente associada apenas a sonhos.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=1)

    QUESTAO('''02) Explique o papel do complexo de Édipo na teoria psicanalítica de Freud e como ele se manifesta na infância.
        A) Estágio do desenvolvimento infantil sem relevância na teoria freudiana.
        B) Conflito entre desejos inconscientes e impulsos conscientes.
        C) Dinâmica entre a criança, seus pais e o surgimento de desejos amorosos ou hostis em relação a eles.
        D) Fase em que a criança supera completamente suas relações familiares.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) O que é o conceito de transferência na psicanálise e como ele se manifesta na relação entre o paciente e o analista?
        A) Processo de transferir responsabilidades do paciente para o analista.
        B) Dinâmica em que o paciente projeta emoções e sentimentos não resolvidos em relação a figuras do passado no analista.
        C) Etapa do tratamento em que o paciente é transferido para outro terapeuta.
        D) Fenômeno que ocorre apenas em sessões de psicanálise em grupo.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Explique o conceito de pulsão de morte (Thanatos) na teoria psicanalítica de Freud.
        A) Impulso instintivo que busca a sobrevivência e a autopreservação.
        B) Energia psíquica associada apenas a comportamentos agressivos.
        C) Força que impulsiona o indivíduo em direção à autorrealização.
        D) Tendência intrínseca à autodestruição e à busca da não-existência.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=4)

    QUESTAO('''05) Qual é o papel do superego na estrutura da mente proposta por Freud e como ele se desenvolve ao longo da infância?
        A) Instância que busca a satisfação imediata dos impulsos e desejos.
        B) Parte da mente que age de acordo com a lógica e o raciocínio.
        C) Componente moral da personalidade que internaliza normas sociais e valores parentais.
        D) Região da mente exclusivamente relacionada a sonhos.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=5)

    QUESTAO('''06) Explique o conceito de mecanismos de defesa na psicanálise e dê exemplos de como esses mecanismos operam.
        A) Estratégias conscientes para lidar com situações estressantes.
        B) Táticas inconscientes para negar, distorcer ou reprimir pensamentos e sentimentos ameaçadores.
        C) Métodos de relaxamento para reduzir a ansiedade.
        D) Processos de tomada de decisão em situações desafiadoras.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que é o conceito de libido na teoria psicanalítica e como ela se relaciona com o desenvolvimento psicossexual?
        A) Energia psíquica associada à agressividade e hostilidade.
        B) Força motivadora que impulsiona o comportamento em direção à busca de prazer.
        C) Aspecto da mente ligado apenas à razão e à lógica.
        D) Fenômeno que ocorre exclusivamente na adolescência.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) Explique a importância do sonho na psicanálise e como Freud abordou a interpretação dos sonhos.
        A) Fenômeno sem relevância no processo terapêutico.
        B) Processo biológico sem relação com o inconsciente.
        C) Manifestação simbólica de desejos inconscientes, acessíveis através da análise.
        D) Método exclusivo de comunicação entre o analista e o paciente.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) O que é a fase anal no desenvolvimento psicossexual proposto por Freud e como ela influencia a personalidade?
        A) Estágio em que a criança desenvolve a sexualidade adulta.
        B) Período associado ao desenvolvimento do controle dos esfíncteres e à formação de traços de personalidade.
        C) Fase em que a criança experimenta o complexo de Édipo.
        D) Etapa exclusiva da adolescência.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) Qual é a crítica mais comum à teoria psicanalítica de Freud e como a psicanálise evoluiu ao longo do tempo em resposta a essas críticas?
        A) Crítica à ênfase excessiva na sexualidade e ao método de tratamento.
        B) Rejeição da importância do inconsciente na vida mental.
        C) Aceitação irrestrita de todas as propostas freudianas.
        D) Desconsideração da importância da infância no desenvolvimento psicológico.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=10)

    FIM()
