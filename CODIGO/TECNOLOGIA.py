from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def tecnologia():
    INICIO(MATERIA="TECNOLOGIA")

    QUESTAO('''01) O que significa a sigla "HTML" no contexto da programação web?
        A) HyperText Markup Language.
        B) High-level Text Manipulation Language.
        C) Hyperlink and Text Management Language.
        D) High-level Markup and Text Language.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=1)

    QUESTAO('''02) Qual é a linguagem de programação mais utilizada para desenvolvimento de aplicativos móveis para iOS?
        A) Java.
        B) Swift.
        C) Kotlin.
        D) Objective-C.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) O que é o conceito de "Machine Learning" (Aprendizado de Máquina) na área de inteligência artificial?
        A) Técnica de ensinar máquinas a programar.
        B) Capacidade de máquinas aprenderem padrões e tomar decisões sem serem explicitamente programadas.
        C) Processo de fabricação de computadores.
        D) Método de treinamento intensivo para programadores.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Qual é a principal característica do sistema operacional Linux?
        A) Desenvolvido pela Microsoft.
        B) Código-fonte fechado.
        C) Interface gráfica exclusiva.
        D) Código-fonte aberto.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=4)

    QUESTAO('''05) O que é "Cloud Computing" (Computação em Nuvem)?
        A) Uso exclusivo de computadores locais.
        B) Utilização de redes sociais.
        C) Armazenamento e processamento de dados em servidores remotos, acessados pela internet.
        D) Manipulação de dados offline.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=5)

    QUESTAO('''06) Qual é o protocolo de comunicação utilizado para transferência de arquivos na internet?
        A) HTTP.
        B) FTP.
        C) TCP.
        D) UDP.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que significa a sigla "URL"?
        A) Universal Resource Locator.
        B) Uniform Resource Language.
        C) Unified Resource Locator.
        D) Universal Reference Language.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=7)

    QUESTAO('''08) Qual é o principal propósito do protocolo HTTPS em uma conexão web?
        A) Comprimir dados para transferência mais rápida.
        B) Criptografar dados para maior segurança.
        C) Limitar o acesso a determinados sites.
        D) Acelerar o carregamento de páginas web.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) O que é "Big Data"?
        A) Computadores de grande porte.
        B) Conjunto de dados de pequeno porte.
        C) Análise de dados em tempo real.
        D) Grande volume de dados, estruturados e não estruturados, que inundam uma organização.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=9)

    QUESTAO('''10) O que significa a sigla "VPN"?
        A) Virtual Private Network.
        B) Very Private Network.
        C) Visual Processing Network.
        D) Virtual Processing Node.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=10)

    FIM()
