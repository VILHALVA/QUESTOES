from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def marketing():
    INICIO(MATERIA="MARKETING")

    QUESTAO('''01) O que é branding no contexto do marketing?
        A) Técnica de venda agressiva.
        B) Estratégia de construção e gestão da marca.
        C) Promoção de produtos apenas por preço baixo.
        D) Segmentação de mercado.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) O que é o mix de marketing (4Ps)?
        A) Produto, Preço, Praça e Participação.
        B) Produto, Praça, Promoção e Produção.
        C) Promoção, Preço, Participação e Produto.
        D) Produção, Praça, Participação e Preço.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=2)

    QUESTAO('''03) O que é pesquisa de mercado?
        A) Análise de concorrência.
        B) Estudo das tendências de produção.
        C) Coleta e análise de informações sobre consumidores e mercado.
        D) Estratégia de precificação.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=3)

    QUESTAO('''04) O que é o conceito de segmentação de mercado?
        A) Divisão do mercado em setores geográficos.
        B) Divisão do mercado em grupos homogêneos de consumidores com características e necessidades semelhantes.
        C) Ações para expandir o mercado.
        D) Estratégia para conquistar novos clientes.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=4)

    QUESTAO('''05) O que é o marketing de conteúdo?
        A) Estratégia de venda de produtos sem informações adicionais.
        B) Criação e distribuição de conteúdo relevante para atrair e engajar o público-alvo.
        C) Publicidade tradicional em mídias de massa.
        D) Estratégia focada apenas na promoção de produtos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) O que é o funil de vendas (funil de conversão)?
        A) Representação gráfica da oferta e demanda.
        B) Processo que transforma consumidores em produtores.
        C) Representação das etapas que um consumidor passa desde o primeiro contato até a compra.
        D) Estratégia de venda exclusiva para clientes recorrentes.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=6)

    QUESTAO('''07) O que é o marketing digital?
        A) Promoção de produtos apenas em canais offline.
        B) Estratégias de marketing exclusivamente em redes sociais.
        C) Utilização de canais online para promover produtos e serviços.
        D) Publicidade tradicional em mídias de massa.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=7)

    QUESTAO('''08) O que é o conceito de persona no marketing?
        A) Representação visual da marca.
        B) Grupo demográfico específico.
        C) Personagem fictício que representa o cliente ideal.
        D) Estratégia de precificação baseada na personalidade do cliente.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) O que é o marketing de relacionamento?
        A) Estratégia focada apenas em vendas rápidas.
        B) Construção e manutenção de relacionamentos duradouros com os clientes.
        C) Promoção de produtos sem interação com os consumidores.
        D) Publicidade agressiva.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) O que é o conceito de benchmarking?
        A) Estratégia de fixação de preços.
        B) Análise e comparação das práticas de outras empresas para melhorar o desempenho.
        C) Estratégia de promoção baseada em competições.
        D) Estratégia de marketing exclusiva para grandes empresas.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
