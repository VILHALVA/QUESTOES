from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def geografia_do_brasil():
    INICIO(MATERIA="GEOGRAFIA DO BRASIL")

    QUESTAO('''01) Qual é o rio mais extenso do Brasil?
        A) Rio Paraná
        B) Rio São Francisco
        C) Rio Amazonas
        D) Rio Tocantins
    ''')
    RESPOSTA(CERTA="C", QUESTAO=1)

    QUESTAO('''02) Em qual região do Brasil está localizado o Pantanal?
        A) Norte
        B) Nordeste
        C) Centro-Oeste
        D) Sudeste
    ''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) Qual é o ponto mais alto do Brasil?
        A) Pico da Neblina
        B) Pico Paraná
        C) Pico das Agulhas Negras
        D) Pico do Itatiaia
    ''')
    RESPOSTA(CERTA="A", QUESTAO=3)

    QUESTAO('''04) Qual é a capital do estado do Amazonas?
        A) Belo Horizonte
        B) Porto Alegre
        C) Manaus
        D) Recife
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) Em qual bioma brasileiro está localizado o Monte Roraima?
        A) Amazônia
        B) Cerrado
        C) Caatinga
        D) Campos Sulinos
    ''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) Qual é a maior cidade do Brasil em termos de população?
        A) Rio de Janeiro
        B) São Paulo
        C) Brasília
        D) Salvador
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) Qual é a única capital brasileira localizada na região Nordeste que não está no litoral?
        A) Natal
        B) Maceió
        C) João Pessoa
        D) Teresina
    ''')
    RESPOSTA(CERTA="D", QUESTAO=7)

    QUESTAO('''08) Em qual estado brasileiro está localizada a Chapada dos Veadeiros?
        A) Bahia
        B) Goiás
        C) Minas Gerais
        D) Tocantins
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) Qual é a ilha brasileira mais próxima do continente africano?
        A) Fernando de Noronha
        B) Ilha Grande
        C) Ilha do Bananal
        D) Trindade
    ''')
    RESPOSTA(CERTA="D", QUESTAO=9)

    QUESTAO('''10) Em qual região do Brasil está localizado o Parque Nacional dos Lençóis Maranhenses?
        A) Norte
        B) Nordeste
        C) Sudeste
        D) Sul
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
