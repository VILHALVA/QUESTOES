from config import *

def sociologia():
    INICIO(MATERIA="SOCIOLOGIA")

    QUESTAO('''01) Qual é o conceito de "anomia" na sociologia, desenvolvido por Émile Durkheim?
        A) Conflito de classes sociais.
        B) Ausência de regras sociais.
        C) Desigualdade de gênero.
        D) Mobilidade social.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) O que é a "mais-valia" na teoria marxista?
        A) Lucro obtido com a venda de produtos.
        B) Valor agregado aos produtos.
        C) Valor do salário mínimo.
        D) Lucro obtido com a exploração do trabalho.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=2)

    QUESTAO('''03) Qual é o conceito de "cultura" na sociologia?
        A) Características biológicas de uma população.
        B) Valores, crenças e práticas compartilhadas por um grupo.
        C) Moda e entretenimento.
        D) Formas de governo em uma sociedade.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) O que é "individualismo" na sociologia?
        A) Forte ligação comunitária.
        B) Supremacia do coletivo sobre o indivíduo.
        C) Valorização da autonomia e liberdade individual.
        D) Igualdade social absoluta.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) O que é "estratificação social"?
        A) Integração entre diferentes classes sociais.
        B) Mobilidade social ascendente.
        C) Divisão da sociedade em camadas ou estratos.
        D) Igualdade total entre os membros da sociedade.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=5)

    QUESTAO('''06) Quem é considerado o fundador da sociologia, conhecido por suas obras "O Contrato Social" e "Discurso sobre a Origem e os Fundamentos da Desigualdade entre os Homens"?
        A) Karl Marx.
        B) Max Weber.
        C) Auguste Comte.
        D) Jean-Jacques Rousseau.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=6)

    QUESTAO('''07) O que é "capital cultural" na sociologia?
        A) Recursos financeiros acumulados ao longo da vida.
        B) Conhecimentos e habilidades adquiridos por meio da educação.
        C) Posses materiais de uma pessoa.
        D) Participação ativa em grupos sociais.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) Qual é o conceito de "alienação" na teoria marxista?
        A) Integração plena do indivíduo na sociedade.
        B) Desenvolvimento pleno das capacidades individuais.
        C) Isolamento e perda de controle sobre o trabalho.
        D) Forte identificação com as instituições sociais.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) O que é "etnocentrismo" na sociologia?
        A) Valorização da diversidade cultural.
        B) Avaliação de outras culturas a partir dos padrões da própria.
        C) Promoção da tolerância cultural.
        D) Integração harmoniosa entre diferentes grupos étnicos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) Qual é o conceito de "sociedade de consumo"?
        A) Sociedade baseada apenas em trocas materiais.
        B) Sociedade que valoriza a autossuficiência.
        C) Sociedade em que o consumo de bens e serviços é central.
        D) Sociedade sem classes sociais.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=10)

    FIM()
