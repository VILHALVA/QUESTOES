from config import *

def jornalismo():
    INICIO(MATERIA="JORNALISMO")

    QUESTAO('''01) Qual é o papel do ombudsman em um veículo de comunicação e como ele contribui para a ética jornalística?
        A) Profissional responsável pela elaboração de manchetes impactantes.
        B) Leitor crítico designado para avaliar a qualidade e ética das reportagens, representando os interesses do público.
        C) Editor-chefe encarregado de supervisionar todos os aspectos da produção jornalística.
        D) Jornalista investigativo responsável pela exposição de irregularidades na redação.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Explique o conceito de jornalismo cidadão (citizen journalism) e como ele se relaciona com as transformações na produção de notícias.
        A) Modelo de jornalismo que ignora a participação do público na produção de conteúdo.
        B) Prática jornalística centrada apenas em fontes oficiais.
        C) Envolvimento ativo do público na coleta, análise e disseminação de informações, muitas vezes por meio das redes sociais.
        D) Estratégia de marketing para atrair novos leitores.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) O que é o fact-checking e qual é a importância dessa prática no jornalismo contemporâneo?
        A) Método de revisão gramatical de textos jornalísticos.
        B) Verificação de fatos para garantir a precisão e autenticidade das informações divulgadas.
        C) Estratégia de marketing para promover notícias sensacionalistas.
        D) Abordagem jornalística que prioriza a subjetividade.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Explique o conceito de pauta jornalística e como ela é elaborada na redação de um veículo de comunicação.
        A) Lista de anúncios publicitários a serem inseridos nas páginas do jornal.
        B) Roteiro para a produção de programas televisivos.
        C) Agenda de temas e assuntos a serem cobertos pelos jornalistas.
        D) Método de seleção de notícias para uma edição específica.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) Qual é a importância do lead em uma reportagem jornalística e como ele deve ser estruturado para atrair a atenção do leitor?
        A) Trecho final da matéria que resume os principais pontos.
        B) Introdução que apresenta os fatos mais relevantes da notícia.
        C) Resumo de opiniões de especialistas.
        D) Seção dedicada a citações de fontes.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) O que é a objetividade no jornalismo e como os jornalistas podem buscar essa característica em suas reportagens?
        A) Tendência a destacar apenas os aspectos positivos das notícias.
        B) Neutralidade na apresentação dos fatos, evitando viés e opiniões pessoais.
        C) Foco exclusivo em notícias negativas para gerar impacto emocional.
        D) Estratégia de destacar apenas os pontos de vista do veículo de comunicação.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) Explique o conceito de "churnalism" e como ele se relaciona com a produção de notícias na era digital.
        A) Prática jornalística que valoriza a profundidade na apuração de fatos.
        B) Tendência de produzir notícias baseadas em press releases e releases de empresas, sem uma investigação aprofundada.
        C) Estratégia de cobertura jornalística centrada apenas em eventos locais.
        D) Abordagem que busca ampliar o espaço dedicado a reportagens investigativas.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) O que são as fake news e quais são os desafios enfrentados pelo jornalismo na era da desinformação?
        A) Notícias verdadeiras que geram controvérsia.
        B) Estratégia de marketing para atrair leitores.
        C) Informações falsas ou enganosas disseminadas como notícias verdadeiras.
        D) Prática jornalística centrada apenas em assuntos polêmicos.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) Qual é a função do editor de fotografia em um veículo de comunicação e como as imagens contribuem para a narrativa jornalística?
        A) Responsável por selecionar as imagens mais sensacionalistas.
        B) Profissional encarregado apenas da parte estética das páginas.
        C) Seleção e edição de fotografias para complementar e enriquecer a narrativa das reportagens.
        D) Atuação exclusiva na área de design gráfico.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) Explique o conceito de jornalismo investigativo e como esse tipo de reportagem contribui para a função social do jornalismo.
        A) Abordagem jornalística centrada apenas em notícias positivas.
        B) Prática de produzir matérias baseadas apenas em opiniões de especialistas.
        C) Atuação exclusiva na cobertura de eventos locais.
        D) Investigação aprofundada de temas relevantes para a sociedade, muitas vezes envolvendo denúncias de corrupção e irregularidades.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=10)

    FIM()
