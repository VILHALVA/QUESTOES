from config import *

def politica():
    INICIO(MATERIA="POLÍTICA")

    QUESTAO('''01) O que é a separação dos poderes, um dos princípios fundamentais da organização política moderna?
        A) Concentração de todos os poderes em um único órgão.
        B) Divisão dos poderes em executivo, legislativo e judiciário.
        C) Exclusão do poder judiciário.
        D) Hierarquia entre os poderes executivo e legislativo.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) O que é um estado de bem-estar social?
        A) Estado que garante apenas a segurança nacional.
        B) Estado que prioriza o bem-estar material dos cidadãos por meio de políticas sociais.
        C) Estado que não interfere na economia.
        D) Estado que concede privilégios apenas a determinadas classes sociais.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) O que é o sistema parlamentarista de governo?
        A) Sistema em que o chefe de estado é eleito pelo povo.
        B) Sistema em que o chefe de governo é independente do parlamento.
        C) Sistema em que o chefe de estado e o chefe de governo são cargos distintos, sendo o primeiro simbólico.
        D) Sistema em que o parlamento não exerce nenhum papel na formação do governo.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=3)

    QUESTAO('''04) O que é o presidencialismo?
        A) Sistema em que o presidente é eleito indiretamente pelo parlamento.
        B) Sistema em que o presidente é chefe de estado e de governo, eleito pelo povo.
        C) Sistema em que o presidente é apenas chefe de estado.
        D) Sistema em que o presidente é vitalício.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=4)

    QUESTAO('''05) O que é um regime democrático?
        A) Sistema em que o poder é exercido por uma única pessoa.
        B) Sistema em que o poder é exercido pelo partido único.
        C) Sistema em que o poder é exercido pelo povo, por meio de representantes eleitos.
        D) Sistema em que o poder é exercido por uma elite econômica.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=5)

    QUESTAO('''06) O que é o voto obrigatório?
        A) Sistema em que o voto é facultativo.
        B) Sistema em que o voto é obrigatório para todos os cidadãos aptos.
        C) Sistema em que o voto é obrigatório apenas para mulheres.
        D) Sistema em que o voto é obrigatório apenas para idosos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que é o federalismo?
        A) Concentração de poder no governo central.
        B) Divisão de poderes entre governos locais e um governo central.
        C) Governo único para todos os estados.
        D) Ausência de governo central.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) O que é um partido político?
        A) Grupo de pessoas que compartilham os mesmos interesses, mas não se envolvem em política.
        B) Grupo de pessoas que se reúnem para praticar esportes.
        C) Organização formada por cidadãos que compartilham ideias e buscam influenciar as decisões políticas.
        D) Grupo de pessoas que se opõem ao sistema democrático.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) O que é a laicidade do Estado?
        A) Estado que privilegia uma religião específica.
        B) Estado que não reconhece a liberdade religiosa.
        C) Separação entre Estado e instituições religiosas, garantindo a liberdade religiosa.
        D) Estado que obriga a participação em práticas religiosas.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) O que é o conceito de Estado de Direito?
        A) Estado em que a lei não é aplicada.
        B) Estado em que o governo é acima da lei.
        C) Estado em que todos, incluindo governantes, estão submetidos à lei.
        D) Estado em que a lei é aplicada apenas a determinadas classes sociais.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=10)

    FIM()
