from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def portugues():
    INICIO(MATERIA="PORTUGUÊS")

    QUESTAO('''01) Identifique a alternativa em que a palavra destacada está empregada em sentido figurado:
        A) O sol está quente hoje.
        B) O tempo voa quando estou me divertindo.
        C) Ele plantou flores no jardim.
        D) A água do rio está limpa.''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Assinale a opção em que a concordância verbal está correta:
        A) Haviam muitos alunos na sala.
        B) Fazem dois anos que não o vejo.
        C) Trata-se de questões delicadas.
        D) Devem existir várias razões para isso.''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) Identifique a alternativa em que a regência nominal está correta:
        A) Ele é avesso de estudar.
        B) Este é o filme que gosto.
        C) Obediente as ordens do chefe.
        D) Fui à festa com minha amiga.''')
    RESPOSTA(CERTA="D", QUESTAO=3)

    QUESTAO('''04) Marque a opção que apresenta uma figura de linguagem diferente nas expressões destacadas:
        A) "Rir é o melhor remédio."
        B) "Essa notícia caiu como uma bomba."
        C) "O sol se despediu no horizonte."
        D) "A cidade acordou sob um manto de neblina."''')
    RESPOSTA(CERTA="A", QUESTAO=4)

    QUESTAO('''05) Qual é a função predominante do pronome "se" na frase "Ela se arrependeu do que fez."?
        A) Pronome reflexivo.
        B) Pronome pessoal oblíquo.
        C) Pronome demonstrativo.
        D) Pronome possessivo.''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) Identifique a figura de linguagem presente na expressão "mar de gente."
        A) Metáfora.
        B) Metonímia.
        C) Pleonasmo.
        D) Antítese.''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) Qual exemplo ilustra o uso de um conector na coesão textual?
        A) "O texto, portanto, aborda temas relevantes."
        B) "O texto contém informações detalhadas."
        C) "O texto apresenta diferentes perspectivas."
        D) "O texto é estruturado de maneira coerente."''')
    RESPOSTA(CERTA="A", QUESTAO=7)

    QUESTAO('''08) Na oração "Ele chegou, e todos aplaudiram," qual é a conjunção utilizada?
        A) Aditiva.
        B) Conclusiva.
        C) Alternativa.
        D) Adversativa.''')
    RESPOSTA(CERTA="A", QUESTAO=8)

    QUESTAO('''09) No trecho "Era uma vez...", identifique a figura de linguagem presente.
        A) Anáfora.
        B) Epíteto.
        C) Hipérbole.
        D) Eufemismo.''')
    RESPOSTA(CERTA="A", QUESTAO=9)

    QUESTAO('''10) O uso do discurso indireto livre é perceptível na seguinte passagem do texto?
        A) "O personagem pensava consigo mesmo."
        B) "O narrador reproduzia os pensamentos do personagem."
        C) "O diálogo entre os personagens era direto e objetivo."
        D) "Os pensamentos do personagem eram apresentados de forma não mediada."''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
