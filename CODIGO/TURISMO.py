from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def turismo():
    INICIO(MATERIA="TURISMO")

    QUESTAO('''01) O que é um ecoturista e quais são suas principais características?
        A) Alguém que viaja para destinos urbanos.
        B) Viajante que busca experiências em ambientes naturais, promovendo a conservação e sustentabilidade.
        C) Turista que se foca apenas em atividades culturais.
        D) Pessoa que viaja exclusivamente a negócios.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Qual é o conceito de turismo sustentável e por que é importante na indústria do turismo?
        A) Prática de viajar apenas de forma luxuosa.
        B) Modelo de turismo que busca minimizar impactos ambientais e promover o desenvolvimento local.
        C) Viagens exclusivamente para destinos exóticos.
        D) Promoção de viagens de curto prazo.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) O que é um destino turístico inteligente e como a tecnologia contribui para esse conceito?
        A) Destino que atrai turistas com eventos esportivos.
        B) Destino que utiliza tecnologias para melhorar a experiência do turista e a eficiência dos serviços.
        C) Local turístico que evita o uso de tecnologia.
        D) Destino voltado exclusivamente para turismo de aventura.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=3)

    QUESTAO('''04) Qual é a importância do guia de turismo em uma viagem e quais são suas responsabilidades?
        A) Auxiliar apenas em questões burocráticas da viagem.
        B) Orientar os turistas, fornecer informações sobre a região e garantir a segurança do grupo.
        C) Apenas conduzir os turistas de um local para outro.
        D) Atuar como tradutor em viagens internacionais.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=4)

    QUESTAO('''05) O que é o turismo de negócios e como ele difere do turismo de lazer?
        A) Viagens realizadas por motivos exclusivamente comerciais.
        B) Viagens voltadas apenas para entretenimento e recreação.
        C) Modelo de turismo que visa explorar culturas diferentes.
        D) Prática de viajar para destinos com belas praias.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=5)

    QUESTAO('''06) O que são os Objetivos de Desenvolvimento Sustentável (ODS) no contexto do turismo?
        A) Metas globais para promover a competição entre destinos turísticos.
        B) Compromissos para promover o turismo de luxo.
        C) Metas estabelecidas pela ONU para orientar práticas sustentáveis na indústria do turismo.
        D) Diretrizes para a privatização de atrações turísticas.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=6)

    QUESTAO('''07) O que é o turismo cultural e qual é a importância da preservação do patrimônio cultural em destinos turísticos?
        A) Prática de visitar museus exclusivamente.
        B) Modalidade de turismo que valoriza a experiência cultural, histórica e artística.
        C) Viagens apenas para destinos religiosos.
        D) Despreocupação com a preservação de monumentos históricos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) O que são parques nacionais e qual é a sua importância no turismo e conservação ambiental?
        A) Áreas protegidas voltadas exclusivamente para atividades industriais.
        B) Reservas naturais que promovem a conservação da biodiversidade e oferecem oportunidades para turismo sustentável.
        C) Áreas de recreação sem importância ecológica.
        D) Espaços naturais que não recebem visitantes.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) O que é o turismo de aventura e quais são algumas atividades associadas a essa modalidade?
        A) Prática de turismo em grandes cidades.
        B) Modalidade que envolve atividades físicas e exploratórias em ambientes naturais.
        C) Viagens apenas para resorts.
        D) Atividades exclusivas em parques temáticos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=9)

    QUESTAO('''10) O que é o turismo gastronômico e por que tem ganhado popularidade nos últimos anos?
        A) Viagens voltadas apenas para experimentar comidas exóticas.
        B) Modalidade que explora a culinária local como parte integrante da experiência turística.
        C) Prática de viajar apenas para restaurantes renomados.
        D) Desinteresse na culinária durante viagens.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
