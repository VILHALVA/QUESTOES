from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def quimica():
    INICIO(MATERIA="QUÍMICA")

    QUESTAO('''01) Qual é a função principal de um catalisador em uma reação química?
        A) Aumentar a temperatura da reação.
        B) Diminuir a velocidade da reação.
        C) Aumentar a energia de ativação.
        D) Diminuir a energia de ativação.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=1)

    QUESTAO('''02) Qual é o nome da reação química em que ocorre a combinação de um elemento com o oxigênio?
        A) Combustão.
        B) Síntese.
        C) Decomposição.
        D) Neutralização.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=2)

    QUESTAO('''03) O que representa a fórmula química H2O?
        A) Ácido sulfúrico.
        B) Dióxido de carbono.
        C) Água.
        D) Amônia.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=3)

    QUESTAO('''04) Qual é o número atômico do carbono?
        A) 6
        B) 8
        C) 12
        D) 14
    ''')
    RESPOSTA(CERTA="A", QUESTAO=4)

    QUESTAO('''05) O que representa o pH em uma solução?
        A) A concentração de íons positivos.
        B) A acidez ou basicidade.
        C) A densidade da solução.
        D) A temperatura da solução.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) Qual é a fórmula química do ácido clorídrico?
        A) HCl
        B) H2SO4
        C) NaOH
        D) CH4
    ''')
    RESPOSTA(CERTA="A", QUESTAO=6)

    QUESTAO('''07) Qual é a principal função dos íons em uma solução iônica?
        A) Conduzir eletricidade.
        B) Neutralizar a solução.
        C) Reduzir a temperatura da solução.
        D) Aumentar a pressão da solução.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=7)

    QUESTAO('''08) Qual é o nome do processo de separação de misturas que se baseia na diferença de volatilidade dos componentes?
        A) Destilação.
        B) Filtração.
        C) Decantação.
        D) Sublimação.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=8)

    QUESTAO('''09) Qual é a função dos isótopos de um elemento químico?
        A) Determinar a reatividade do elemento.
        B) Diferenciar os compostos formados pelo elemento.
        C) Contribuir para a massa atômica do elemento.
        D) Estabelecer a cor do elemento.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) O que representa a lei da conservação da massa em uma reação química?
        A) A massa total dos reagentes é igual à massa total dos produtos.
        B) A massa total dos reagentes é maior que a massa total dos produtos.
        C) A massa total dos reagentes é menor que a massa total dos produtos.
        D) A massa dos produtos é sempre zero.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=10)

    FIM()
