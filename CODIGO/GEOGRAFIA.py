from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def geografia():
    INICIO(MATERIA="GEOGRAFIA")

    QUESTAO('''01) Qual é o maior oceano do planeta?
        A) Atlântico.
        B) Índico.
        C) Antártico.
        D) Pacífico.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=1)

    QUESTAO('''02) O que são cordilheiras?
        A) Planícies alagadas.
        B) Cadeias de montanhas.
        C) Grandes desertos.
        D) Florestas tropicais.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=2)

    QUESTAO('''03) Qual é o principal gás responsável pelo efeito estufa?
        A) Metano.
        B) Oxigênio.
        C) Dióxido de carbono.
        D) Hidrogênio.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=3)

    QUESTAO('''04) O que caracteriza uma economia de subsistência?
        A) Produção voltada para o mercado internacional.
        B) Agricultura de exportação.
        C) Produção para atender às necessidades locais.
        D) Uso intensivo de tecnologia.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=4)

    QUESTAO('''05) O que é um bioma?
        A) Uma unidade monetária.
        B) Uma comunidade de organismos interagindo com o ambiente.
        C) Uma fronteira política.
        D) Uma área de influência cultural.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) Qual é o principal rio da Amazônia?
        A) Paraná.
        B) Solimões.
        C) Tocantins.
        D) São Francisco.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=6)

    QUESTAO('''07) O que é desertificação?
        A) Expansão de desertos.
        B) Deslocamento de placas tectônicas.
        C) Transformação de áreas verdes em desertos.
        D) Processo de formação de dunas.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=7)

    QUESTAO('''08) Qual é a camada mais externa da Terra?
        A) Manto.
        B) Núcleo externo.
        C) Crosta.
        D) Núcleo interno.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) O que é globalização?
        A) Integração de países em organizações militares.
        B) Isolamento de nações.
        C) Integração econômica e cultural entre países.
        D) Restrição de trocas comerciais.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) O que é um tsunami?
        A) Tempestade tropical.
        B) Onda gigante causada por atividade sísmica no oceano.
        C) Ventos fortes em áreas montanhosas.
        D) Inundação de rios.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
