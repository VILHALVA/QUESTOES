from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def artes():
    INICIO(MATERIA="ARTES")

    QUESTAO('''01) Qual movimento artístico foi caracterizado por formas geométricas, cores vibrantes e abstração?
        A) Renascimento.
        B) Cubismo.
        C) Barroco.
        D) Impressionismo.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=1)

    QUESTAO('''02) Quem foi o pintor italiano famoso por obras como "O Nascimento de Vênus" e "Primavera"?
        A) Leonardo da Vinci.
        B) Michelangelo.
        C) Raphael.
        D) Sandro Botticelli.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=2)

    QUESTAO('''03) Qual estilo arquitetônico foi predominante na Idade Média, caracterizado por arcos ogivais e vitrais coloridos?
        A) Gótico.
        B) Românico.
        C) Renascentista.
        D) Neoclássico.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=3)

    QUESTAO('''04) Quem foi o principal representante do surrealismo, conhecido por obras como "A Persistência da Memória"?
        A) Salvador Dalí.
        B) Pablo Picasso.
        C) Joan Miró.
        D) René Magritte.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=4)

    QUESTAO('''05) O que é a técnica de "chiaroscuro" nas artes visuais?
        A) Uso de cores vibrantes.
        B) Contraste entre luz e sombra.
        C) Uso de formas geométricas.
        D) Representação realista de objetos.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) Quem foi o escritor brasileiro modernista conhecido por obras como "Macunaíma"?
        A) Machado de Assis.
        B) Lima Barreto.
        C) Mário de Andrade.
        D) Carlos Drummond de Andrade.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=6)

    QUESTAO('''07) Qual é a principal característica do teatro do absurdo?
        A) Uso intensivo de efeitos especiais.
        B) Narrativas lineares e tradicionais.
        C) Diálogos complexos e profundos.
        D) Representação de situações absurdas e sem sentido.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=7)

    QUESTAO('''08) Qual é a técnica de pintura que utiliza água e pigmentos sobre papel molhado, criando efeitos fluidos e espontâneos?
        A) Óleo sobre tela.
        B) Aquarela.
        C) Guache.
        D) Têmpera.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=8)

    QUESTAO('''09) Quem foi um dos principais representantes do expressionismo alemão na pintura, com obras como "O Grito"?
        A) Wassily Kandinsky.
        B) Gustav Klimt.
        C) Edvard Munch.
        D) Egon Schiele.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=9)

    QUESTAO('''10) O que é a técnica de "mármore" na escultura?
        A) Uso de mármore como material.
        B) Imitação visual de mármore em outros materiais.
        C) Técnica de esculpir diretamente no mármore.
        D) Técnica de marmorização de pinturas.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=10)

    FIM()
