from AAAA import INICIO
from AAAA import QUESTAO
from AAAA import RESPOSTA
from AAAA import FIM

def filosofia():
    INICIO(MATERIA="FILOSOFIA")

    QUESTAO('''01) Qual filósofo grego é considerado o "Pai da Filosofia"?
        A) Sócrates.
        B) Platão.
        C) Aristóteles.
        D) Tales de Mileto.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=1)

    QUESTAO('''02) Qual é a principal obra de Immanuel Kant?
        A) "Ética a Nicômaco".
        B) "O Príncipe".
        C) "Crítica da Razão Pura".
        D) "Meditações".
    ''')
    RESPOSTA(CERTA="C", QUESTAO=2)

    QUESTAO('''03) O que significa a expressão latina "Cogito, ergo sum"?
        A) Penso, logo existo.
        B) A vida é breve.
        C) Deus está morto.
        D) A essência precede a existência.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=3)

    QUESTAO('''04) Quem é considerado o fundador do Existencialismo?
        A) Friedrich Nietzsche.
        B) Jean-Paul Sartre.
        C) Albert Camus.
        D) Søren Kierkegaard.
    ''')
    RESPOSTA(CERTA="D", QUESTAO=4)

    QUESTAO('''05) Qual filósofo foi discípulo de Platão e preceptor de Alexandre, o Grande?
        A) Sócrates.
        B) Aristóteles.
        C) Tales de Mileto.
        D) Anaximandro.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=5)

    QUESTAO('''06) O que é o método cartesiano proposto por René Descartes?
        A) Método dialético.
        B) Método fenomenológico.
        C) Método da dúvida metódica.
        D) Método hermenêutico.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=6)

    QUESTAO('''07) Quem é o autor da obra "O Contrato Social"?
        A) John Locke.
        B) Jean-Jacques Rousseau.
        C) Thomas Hobbes.
        D) Montesquieu.
    ''')
    RESPOSTA(CERTA="B", QUESTAO=7)

    QUESTAO('''08) O que significa a expressão "Tabula Rasa" na filosofia empirista?
        A) Tábua de conhecimento.
        B) Tábula de valores.
        C) Tábula de experiências.
        D) Tábula de ética.
    ''')
    RESPOSTA(CERTA="C", QUESTAO=8)

    QUESTAO('''09) Qual é o conceito central da filosofia de Friedrich Nietzsche?
        A) Vontade de poder.
        B) Estado de natureza.
        C) Alma imortal.
        D) Racionalismo.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=9)

    QUESTAO('''10) Quem é o autor da obra "O Príncipe", que trata da política e do poder?
        A) Maquiavel.
        B) Hobbes.
        C) Rousseau.
        D) Marx.
    ''')
    RESPOSTA(CERTA="A", QUESTAO=10)

    FIM()
